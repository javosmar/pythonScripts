def suma(num1,num2):
    """Devuelve la suma de 2 números"""
    return num1 + num2

def resta(num1,num2):
    """ Devuelve la resta de 2 números"""
    return num1 - num2

def multip(num1,num2):
    """Devuelve el producto de 2 números"""
    return num1 * num2

def div(num1,num2):
    """Devuelve el cociente de 2 números"""
    return num1 / num2